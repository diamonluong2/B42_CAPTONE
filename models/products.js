// Định nghĩa products constructor
function Products(
  id,
  name,
  price,
  screen,
  backCam,
  frontCam,
  img,
  descripton,
  type
) {
  this.id = id;
  this.name = name;
  this.price = price;
  this.screen = screen;
  this.backCam = backCam;
  this.frontCam = frontCam;
  this.img = img;
  this.descripton = descripton;
  this.type = type;
}

